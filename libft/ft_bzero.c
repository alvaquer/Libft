#include "libft.h"

void	ft_bzero(void *ptr, size_t num)
{
	size_t	i;
	char	*ptr1;

	ptr1 = (char *)ptr;
	i = 0;
	while (i < num)
	{
		*ptr1 = 0;
		i++;
		ptr1++;
	}
}
